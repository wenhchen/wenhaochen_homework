package com.example.demo.entity;

public class Student {
private long stu_id;
private String stu_name;
private long stu_contact;
private long stu_class;
public long getStu_id() {
	return stu_id;
}
public void setStu_id(long stu_id) {
	this.stu_id = stu_id;
}
public String getStu_name() {
	return stu_name;
}
public void setStu_name(String stu_name) {
	this.stu_name = stu_name;
}
public long getStu_contact() {
	return stu_contact;
}
public void setStu_contact(long stu_contact) {
	this.stu_contact = stu_contact;
}
public long getStu_class() {
	return stu_class;
}
public void setStu_class(long stu_class) {
	this.stu_class = stu_class;
}
}
